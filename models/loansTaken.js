const sequelize=require('../database/db');
const {DataTypes}=require('sequelize')
const loansTaken=sequelize.define('loansTaken',{
    id:{
        type:DataTypes.INTEGER,
        autoIncrement:true,
        allowNull:false,
        primaryKey:true
    },
    loanType:{
        type:DataTypes.TEXT,
        allowNull:false
    },
    duration:{
        type:DataTypes.STRING,
        allowNull:false
    },
    amount:{
        type:DataTypes.INTEGER,
        allowNull:false
    },
    phone:{
        type:DataTypes.STRING(11),
        allowNull:true
    },
    bank:{
        type:DataTypes.STRING(40),
        allowNull:true,
    },
    account:{
        type:DataTypes.INTEGER(10),
        allowNull:true
    },
    picture:{
        type:DataTypes.STRING,
        allowNull:true
    },
    email:{
        type:DataTypes.STRING(35),
        unique:true,
        allowNull:false
    },
    bvn:{
        type:DataTypes.INTEGER(14),
        allowNull:true,
        unique:true
    }
})

module.exports=loansTaken;